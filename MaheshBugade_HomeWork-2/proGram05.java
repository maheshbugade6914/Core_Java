package HomeWork02;

public class proGram05 {
    public static void main(String[] args){
        int marks = 32;
        if(marks >= 90){
            System.out.println("A+");
        }
        else if(marks >= 75){
            System.out.println("A");
        }
        else if(marks >= 60){
            System.out.println("B");
        }
        else if(marks >= 40){
            System.out.println("C");
        }
        else {
            System.out.println("Fail");
        }
    }
}
